    private boolean verificaQuantidades() {
        Vector<Produto> produtos = carrinhoComprasTab.produtosCarrinho();

        for (int i = 0; i < produtos.size(); i++) {
            Produto estoque = FakeBD.conProdCod(produtos.get(i).getCod());
            if (produtos.get(i).getQnt() > estoque.getQnt()) {
                JOptionPane.showMessageDialog(null, "A quantidade do produto" + estoque.getNome() + "não é suficiente para real. a venda", "Quantidade insuficiente", JOptionPane.INFORMATION_MESSAGE);
                return false;
            }
        }
        return true;
    }
    private void finBtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_finBtActionPerformed

        if (verificaQuantidades() == true) {
            Vector<Produto> produtos = carrinhoComprasTab.produtosCarrinho();

            for (int i = 0; i < produtos.size(); i++) {
                Produto estoque = FakeBD.conProdCod(produtos.get(i).getCod());
                estoque.setQnt(estoque.getQnt() - produtos.get(i).getQnt());

            }
            precoCompra = 0.0;
            totalTxt.setText("0.0");

            carrinhoComprasTab.limpaCarrinho();
            TabelaCC.updateUI();
        }


    }//GEN-LAST:event_finBtActionPerformed

