    private void CodTxtKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_CodTxtKeyTyped
        if (evt.getKeyChar() == '\n') {

            if (!CodTxt.getText().isEmpty()) {
                try {
                    int cod = Integer.parseInt(CodTxt.getText());
                    Produto consultado = FakeBD.conProdCod(cod);

                    if (consultado != null) {
                        NomeTxt.setText(consultado.getNome());
                        PrecoUnitTxt.setText(consultado.getPreco() + "");
                        QuantTxt.setEnabled(true);

                    } else {
                        JOptionPane.showMessageDialog(null, "Produto não cadastrado", "código", JOptionPane.INFORMATION_MESSAGE);
                    }
                } catch (NumberFormatException ex) {
                    JOptionPane.showMessageDialog(null, "O código deve ser um valor númerico", "erro no codigo", JOptionPane.WARNING_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_CodTxtKeyTyped

    private void QuantTxtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_QuantTxtActionPerformed
        //   //calcula o preço parcial dos produtos

        if (!QuantTxt.getText().isEmpty()) {
            try {
                int quantidade = Integer.parseInt(QuantTxt.getText());
                double parc = produtoSelecionado.getPreco() * quantidade;
                PrecoUnitTxt.setText(String.format("%.2f", parc));

            } catch (NumberFormatException ex) {
                PrecoUnitTxt.setText("N/A");
            }
        } else {
            PrecoUnitTxt.setText("N/A");
        }
    }//GEN-LAST:event_QuantTxtActionPerformed

    private void removeBtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeBtActionPerformed

        int linha = TabelaCC.getSelectedRow();
        if (linha > -1) {
            int op = JOptionPane.showConfirmDialog(null, "Deseja remover o produto selecionado?",
                    "Confirmação de exclusão", JOptionPane.YES_NO_OPTION);
            if (op == JOptionPane.YES_OPTION) {
                String senha = JOptionPane.showInputDialog(null, "Informe a senha do gerente",
                        "operação restrita", JOptionPane.INFORMATION_MESSAGE);
                if (senha != null && senha.equals("ifmg")) {
                    carrinhoComprasTab.removeProdutoCarrinho(linha);
                    atualizaQuantidades();
                }
            }

        } else {
            JOptionPane.showMessageDialog(null, "primeiro é necessário selecionar"
                    + " algum produto", "selecionar!!!", JOptionPane.INFORMATION_MESSAGE);
        }

    }//GEN-LAST:event_removeBtActionPerformed

    private void addBtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtActionPerformed
        if (produtoSelecionado != null && !PrecoUnitTxt.getText().equals("N/A")) {

            int quantidade = Integer.parseInt(QuantTxt.getText());

            if (quantidade <= produtoSelecionado.getQnt()) {

                Produto vendido = new Produto(produtoSelecionado.getCod(), produtoSelecionado.getNome(), produtoSelecionado.getPreco(), quantidade);

                //produtoSelecionado.setQnt(produtoSelecionado.getQnt() - quantidade);
                precoCompra += produtoSelecionado.getPreco() * quantidade;
                totalTxt.setText(String.format("%.2f", precoCompra));

                CodTxt.setText("");
                QuantTxt.setText("");
                PrecoUnitTxt.setText("");
                produtoSelecionado = null;
                QuantTxt.setEnabled(false);
                NomeTxt.setText("");

                this.carrinhoComprasTab.addNovoProduto(vendido);
                TabelaCC.updateUI();

            } else {
                JOptionPane.showMessageDialog(null, "Não há quantidade sufuciente para realizar a venda deste produto",
                        "quantidade insuficiente", JOptionPane.INFORMATION_MESSAGE);
            }

        } else {
            JOptionPane.showMessageDialog(null, "A quantidade de produtos informada"
                    + "deve ser um valor numérico", "entrada de quantidade inválida", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_addBtActionPerformed
    public void atualizaQuantidades() {
        TabelaCC.updateUI();

        double preco = carrinhoComprasTab.calculaPrecoParcialCompra();

        totalTxt.setText(String.format("%.2f", preco));
    }
